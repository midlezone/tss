							   <p><strong>Ethereum Coin</strong> chính là<strong>&nbsp;“coin nền tảng”</strong> </a>tốt nhất hiện nay. Một trong những hướng đi <strong>phát triển công nghệ tiền ảo</strong>, giúp cho các đồng coin mới phát triển dựa trên nền tảng Ethereum. Ethereum giúp cho những <strong>coin dạng ứng dụng</strong>, dịch vụ không cần phải triển khai blockchain nữa mà họ có thể sử dụng blockchain của Ethereum để phát triển luôn.</p>
