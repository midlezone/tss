							   <p>Tuy nhiên từ đầu năm 2017 đã có dấu hiệu phát triển của đồng NEO, mọi thứ đang phát triển tốt. NEO vẫn tăng giá theo xu hướng của các <strong>đồng tiền điện tử</a></strong>. Qua đó có thể thấy được vấn đề cấm vận vẫn chỉ là con muỗi đốt.</p>
							   <p><strong>Điểm nổi bật của NEO</strong> là thời gian giao dịch 1000/s so với 15/s của Ethereum. Ngoài ra Neo đảm bảo các vấn đề liên quan đến việc phân tách, đó là một trong những điểm mới so với Ethereum.</p>
