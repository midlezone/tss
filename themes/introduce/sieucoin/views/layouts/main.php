							   <h2><span class="ez-toc-section" id="1_Ripple_XRP">1. Ripple (XRP)</span></h2>
							   <p><strong>Ripple Coin</strong> là một đồng liên quan đến <strong>hoạt động thanh toán theo thời gian thực</strong> hỗ trợ việc thanh toán nhanh chóng giữa các ngân hàng theo<strong> giao thức (RTXP)</strong>. Mục đích Ripple hướng đến chính là<strong> thay thế các giao thức đã lỗi</strong> thời trong các ngân hàng, giúp các ngân hàng tăng độ cạnh tranh về giá phí, giúp người dùng có thể giao dịch nhanh chóng và an toàn hơn.</p>
							   <p><strong>Ripple</strong> cũng <strong>sử dụng nền tảng Blockchain</strong> để triển khai và phát triển chính vì vậy <strong>độ an toàn</strong> cũng như <strong>sự quản lý</strong>&nbsp;được đánh giá cao. Ripple đang là <strong>công ty đầu tiên cung cấp giải pháp thanh toán trên nền tảng Blockchain</strong>. Hiện đã có nhiều ngân hàng bên Nhật và Hàn Quốc đang triển khai nền tảng Ripple Beta. Chính điều đó tạo nên cơn sốt đồng Ripple tăng trưởng mạnh cuối năm 2017.</p>
