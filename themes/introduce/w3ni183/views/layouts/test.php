                                        <div class="newsdes">
                                            <h3>Những chính sách áp dung cho khách hàng mua sản phẩm máy nước nóng năng lương mặt trời tại trung tâm phân phối sản phẩm Sơn Hà như sau :</h3>

                                            <p>&nbsp;&nbsp;1. Tiếp nhận thông tin từ phía khách hàng</p>

                                            <div>- Khi khách hàng liên hệ tới công ty để đặt hàng &nbsp;bằng các hình thức : Đặt hàng trên mạng, gọi điện, mua hàng trực tiếp.. thì bộ phận bán hàng tiếp nhận thông tin đơn hàng của khách hàng. &nbsp;Tư vấn kỹ thuật sản phẩm: Những yêu cầu trước khi lắp sản phẩm thái dương năng, tư vấn lắp đặt đường nước, tư vấn phương án lắp đặt với từng kết cấu ngồi nhà khác nhau,thống nhất giá thành sản phẩm và các chi phí phát sinh khác nếu có. Nếu khách hàng lấy hóa đơn VAT thì yêu cầu khách hàng cung cấp thông tin, hóa đơn VAT sẽ được chuyển &nbsp;sau khi giao hàng, thời gian giao hóa đơn thông thường từ 3-5 ngày (hoặc lâu hơn).</div>

                                            <div><br>
                                                2. Khảo sát vị trí lắp đặt năng lượng Thái dương năng<br>
                                                &nbsp;</div>

                                            <div>- Nếu khách hàng có nhu cầu khảo sát và tư vấn tại nhà trước khi lắp sản phẩm &nbsp;thì bộ phân bán hàng xin lại thông tin để tiến hàng khảo sát trước khi chuyển hàng. Trong quá trình khảo sát nếu phát sinh chi phí khác thì bộ phận khảo sát thống nhất với khách hàng trước và báo cho bộ phận bán hàng.</div>

                                            <div><br>
                                                3. Giao hàng<br>
                                                &nbsp;</div>

                                            <div>- Với sản phẩm Thái dương năng sẽ có các phương thức chuyển hàng sau :</div>

                                            <div>&nbsp; &nbsp;+ Do công ty Cổ Phần Quốc Tế Sơn Hà vận chuyển</div>

                                            <div>&nbsp; &nbsp;+ Do Công ty TNHH Seabig Việt Nam vận chuyển</div>

                                            <div>&nbsp; &nbsp;+ Hoặc do một đơn vị thứ 3 nếu có.</div>

                                            <div>- Bộ phận vận chuyển sắp xếp giao hàng trong ngày đặt hàng (trước khi đi Bộ phận vận chuyển sẽ gọi cho khách hành trước 1h đồng hồ để khách hàng chủ động nhận hàng).</div>

                                            <div><br>
                                                4. Nhận hàng và lắp đặt<br>
                                                &nbsp;</div>

                                            <div>- Khi sản phẩm chuyển tới khách hàng khách hàng cần kiểm tra kỹ xem sản phẩm có đúng chủng loại mình đặt hay không,sản phẩm không bị vỡ ống, không bị bóp méo bình bảo ôn ... Sau đó khách hàng ký vào hóa đơn giao hàng. Nếu sản phẩm không đúng như yêu cầu thì khách hàng có quyền không nhận hàng và yêu cầu đổi lại hàng.</div>

                                            <div>- Sau khi khách hàng nhận hàng thì bộ phân lắp đăt đến và tiến hành lắp đăt &nbsp;cho khách hàng.Nhà sản xuất tiến hành lắp đặt miễn phí trong phạm vi quy định của nhà SX . Bộ phận lắp đặt chỉ lắp đặt hoàn thiện sản phẩm và để đầu chờ có sẵn. Trong trường hợp lắp đặt trên mái nghiêng thì khách hàng phải mất thêm chi phí lắp đặt trên mái nghiêng, các vật dụng để lắp đặt gia cố khách hàng phải chuẩn bị từ trước. &nbsp;Việc nâng bồn, lắp đặt đường nước vào ra khách hàng phải tự hoàn thiện.</div>

                                            <div>Chú ý : &nbsp;&nbsp;<br>
                                                * Phụ kiện đi kèm(loại từ 14-30 ống): 01 van 1 chiều bằng đồng, 04 đầu cút ren vuông trong, 04 cút hàn, 02 măng xông, 01 chếch hàn, 04 m ống nóng, 04m ống lạnh, 01 cuộn băng tan &nbsp;(Chỉ áp dụng tại Hà Nội)<br>
                                                * Đối với hệ giàn công nghiệp, giá trên chưa bao gồm: Chi phí vật tư lắp đặt đường ống cấp nước vào bình bảo ôn, đường cấp nước nóng từ bình ra, chi phí vận chuyển và thiết bị hỗ trợ điện. Cần khảo sát thực tế trước khi báo giá bán khách hàng…</div>

                                            <div><br>
                                                5. Thu tiền.<br>
                                                &nbsp;</div>

                                            <div>- Sau khi lắp đặt xong khách hàng thanh toán đầy đủ tiền hàng cho bộ phận lắp đặt ngay tại thời điểm lắp đặt xong</div>

                                            <div><br>
                                                6. Bảo hành<br>
                                                &nbsp;</div>

                                            <div>-Sản phẩm dòng GOLD và ECO được bảo hành kỹ thuật 05 năm. Sản phẩm dòng Titan được bảo hành kỹ thuật 07 năm.(Thời gian Bảo hành in trực tiếp trên thân sản phẩm. Ko áp dụng phiếu giấy)</div>

                                            <div>- Công ty chỉ bảo hành các sản phẩm trong các điều kiện sau :&nbsp;</div>

                                            <div>&nbsp; &nbsp; &nbsp;+ Sản phẩm còn trong hạn bảo hành</div>

                                            <div>&nbsp; &nbsp; &nbsp;+ Hư hỏng do chất lượng linh kiện hay lỗi quy trình sản xuất.</div>

                                            <div>- Công Ty không bảo hành trong các trường hợp :</div>

                                            <div>&nbsp; &nbsp; + Sản phẩm không còn trong thời gian bảo hành</div>

                                            <div>&nbsp; &nbsp; + Sản phẩm bị hư hỏng do các yếu tố khách quan như thiên tai, lũ lụt....&nbsp;</div>

